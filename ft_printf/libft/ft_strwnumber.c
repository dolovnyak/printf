#include <string.h>

size_t		ft_strwnumber(const char *s)
{
	size_t a;

	a = 0;
	if (!s)
		return (0);
	while (*s == ' ' || *s == '\n' || *s == '\t')
		s++;
	while (*s)
	{
		a++;
		while (*s != ' ' && *s != '\n' && *s != '\t' && *s != '\0')
			s++;
		while (*s == ' ' || *s == '\n' || *s == '\t')
			s++;
		if (*s == '\0')
			return (a);
	}
	return (0);
}
