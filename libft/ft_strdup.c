#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*a;
	size_t	i;

	i = 0;
	if (!s1 || !(a = (char *)malloc(ft_strlen(s1) + 1)))
		return (0);
	while (s1[i])
	{
		a[i] = s1[i];
		i++;
	}
	a[i] = '\0';
	return (a);
}
