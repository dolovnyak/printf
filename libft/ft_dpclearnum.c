#include "libft.h"

void	ft_dpclearnum(char **arr, int size)
{
	int i;

	if (arr && *arr)
	{
		i = 0;
		while (i < size)
		{
			ft_strdel(&(arr[i]));
			i++;
		}
		free(arr);
	}
}
